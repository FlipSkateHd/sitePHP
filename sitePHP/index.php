    <p>É um time de E-sports do jogo League of Legends, conhecido por seu famoso integrante Faker, um dos melhores jogadores do mundo.</p>  
    <p>Originalmente criado em 2002 pelo jogador de Star Craft Lim "BoxeR", e mais tarde em 2004 a operadora SK Telecom começou a patrocinar a equipe criando a SK Telecom T1.</p>
    
    <p>A equipe de League of Legends da T1 tem um recorde de quatro títulos do Campeonato Mundial de League of Legends (2013, 2015, 2016 e 2023) e dez títulos de League of Legends Champions Korea</p>
    
